package com.ict01.grammer03;
class Ex03
{
	public static void main(String[] args)
	{
	 // 대입연산자 : 특정값이나 객체의 주소를 변수에 기억시켜 사용(저장)
	 // 	    (=, +=, -=, *=, /=, %= )
		int s1 = 7 ;
		s1 += 4 ;		// s1 = s1 + 4 ;
		System.out.println("s1 = " + s1) ;

		s1 -= 4 ;		// s1 = s1 - 4 ;
		System.out.println("s1 = " + s1) ;

		s1 *= 4 ;		// s1 = s1 * 4 ;
		System.out.println("s1 = " + s1) ;

		s1 /= 4 ;		// s1 = s1 / 4 ;
		System.out.println("s1 = " + s1) ;

		s1 %= 4 ;		// s1 = s1 % 4 ;
		System.out.println("s1 = " + s1) ;


	}

}